// 종목 검색 (이름 또는 코드로)
export function searchLocalStocks(query: string): StockItem[] {
  if (!query || query.length < 1) return [];

  const q = query.toLowerCase().trim();

  // 중복 제거 (symbol 기준)
  const seen = new Set<string>();
  return STOCK_LIST.filter((stock) => {
    if (seen.has(stock.symbol)) return false;
    const match = stock.name.toLowerCase().includes(q) || stock.symbol.includes(q);
    if (match) seen.add(stock.symbol);
    return match;
  }).slice(0, 15);
}
